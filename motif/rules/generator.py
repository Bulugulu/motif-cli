"""
Rules generation logic for the Motif CLI.

Takes analysis JSON output (from the host agent) and generates configuration files:
CLAUDE.md, skill files, and optionally .cursorrules.
"""

import json
import re
from pathlib import Path


def load_analysis(path: str) -> dict:
    """Load and validate analysis JSON from a file path.

    Handles both raw JSON files and the case where JSON is embedded in
    markdown (extracts JSON from ```json ... ``` code fences if needed).

    Args:
        path: Path to the analysis file (JSON or markdown with embedded JSON).

    Returns:
        Parsed analysis dict with defaults for missing top-level keys.

    Raises:
        FileNotFoundError: If the file does not exist.
        json.JSONDecodeError: If no valid JSON can be extracted.
    """
    p = Path(path)
    if not p.exists():
        raise FileNotFoundError(f"Analysis file not found: {path}")

    text = p.read_text(encoding="utf-8")

    # Try direct JSON parse first
    try:
        return _validate_analysis(json.loads(text))
    except json.JSONDecodeError:
        pass

    # Extract JSON from markdown code fences
    match = re.search(r"```(?:json)?\s*\n([\s\S]*?)\n```", text)
    if match:
        try:
            return _validate_analysis(json.loads(match.group(1)))
        except json.JSONDecodeError as e:
            raise json.JSONDecodeError(
                f"Invalid JSON in markdown code fence: {e.msg}",
                e.doc,
                e.pos,
            ) from e

    raise json.JSONDecodeError(
        "No valid JSON found. Expected raw JSON or JSON in ```json ... ``` code fence.",
        text,
        0,
    )


def _validate_analysis(data: dict) -> dict:
    """Ensure analysis has all expected top-level keys with defaults."""
    defaults = {
        "skills": [],
        "rules": [],
        "communication_style": {},
        "session_patterns": {},
        "improvement_areas": [],
        "project_context": {},
    }
    result = {**defaults, **data}

    # Normalize nested dicts
    if not isinstance(result.get("communication_style"), dict):
        result["communication_style"] = {}
    if not isinstance(result.get("session_patterns"), dict):
        result["session_patterns"] = {}
    if not isinstance(result.get("project_context"), dict):
        result["project_context"] = {}

    return result


def generate_claude_md(analysis: dict, project_name: str = "this project") -> str:
    """Generate a CLAUDE.md file from the analysis.

    Args:
        analysis: Parsed analysis dict from load_analysis.
        project_name: Name of the project for the header.

    Returns:
        Full CLAUDE.md content as a string.
    """
    lines = [
        "# CLAUDE.md",
        "",
        "*Generated by Motif — review and customize for your workflow.*",
        "",
    ]

    # Project Overview
    ctx = analysis.get("project_context") or {}
    desc = ctx.get("description", "").strip()
    if desc:
        lines.extend(["## Project Overview", "", desc, ""])
        entities = ctx.get("key_entities") or []
        if entities:
            lines.append("**Key entities:** " + ", ".join(entities))
            lines.append("")
        tools = ctx.get("tools_used") or []
        if tools:
            lines.append("**Tools used:** " + ", ".join(tools))
            lines.append("")
    else:
        lines.extend(["## Project Overview", "", f"Guidance for {project_name}.", ""])

    # Communication Style
    comm = analysis.get("communication_style") or {}
    if any(comm.values()):
        lines.extend(["## Communication Style", ""])
        for key, val in comm.items():
            if val:
                label = key.replace("_", " ").title()
                lines.append(f"- **{label}:** {val}")
        lines.append("")

    # Agent Behavior / Rules
    rules = analysis.get("rules") or []
    included_rules = [
        r for r in rules
        if (r.get("confidence") or "low") in ("high", "medium")
    ]
    if included_rules:
        lines.extend(["## Agent Behavior", "", "### Rules", ""])
        for i, r in enumerate(included_rules, 1):
            enforces = r.get("enforces", "").strip()
            if enforces:
                lines.append(f"{i}. {enforces}")
        lines.append("")

    # Workflow Triggers
    skills = analysis.get("skills") or []
    if skills:
        lines.extend(["## Workflow Triggers", ""])
        lines.append("| When you're... | Load this file |")
        lines.append("|---|---|")
        for s in skills:
            trigger = (s.get("trigger") or "").strip()
            name = (s.get("name") or "unnamed").strip()
            scope = s.get("scope", "project")
            if scope == "user":
                path = f"~/.cursor/skills/{_slug(name)}/SKILL.md"
            else:
                path = f".cursor/skills/{_slug(name)}/SKILL.md"
            lines.append(f"| {trigger or '(no trigger)'} | `{path}` |")
        lines.append("")

    # Session Patterns
    sess = analysis.get("session_patterns") or {}
    if any(sess.values()):
        lines.extend(["## Session Patterns", ""])
        startup = sess.get("startup", "").strip()
        wrapup = sess.get("wrapup", "").strip()
        if startup:
            lines.append(f"- **Startup:** {startup}")
        if wrapup:
            lines.append(f"- **Wrapup:** {wrapup}")
        evidence = sess.get("evidence") or []
        if evidence:
            lines.append("- **Evidence:**")
            for e in evidence[:3]:
                if e:
                    lines.append(f"  - {e}")
        lines.append("")

    # Improvement Areas
    improvements = analysis.get("improvement_areas") or []
    if improvements:
        lines.extend(["## Improvement Areas", ""])
        for imp in improvements:
            problem = (imp.get("problem") or "").strip()
            proposed = (imp.get("proposed_rule") or "").strip()
            if problem:
                lines.append(f"- **{problem}**")
                if proposed:
                    lines.append(f"  - Proposed rule: {proposed}")
        lines.append("")

    return "\n".join(lines).rstrip() + "\n"


def _slug(name: str) -> str:
    """Convert skill name to a filesystem-safe slug."""
    return "".join(c if c.isalnum() or c in "-_" else "-" for c in name.lower()).strip("-")


def generate_skill_file(skill: dict) -> str:
    """Generate a single skill SKILL.md from a skill dict.

    Args:
        skill: Skill dict with name, trigger, steps, evidence, confidence, etc.

    Returns:
        Full SKILL.md content as a string.
    """
    name = (skill.get("name") or "unnamed").strip()
    trigger = (skill.get("trigger") or "").strip()
    steps = skill.get("steps") or []
    evidence = skill.get("evidence") or []
    confidence = (skill.get("confidence") or "low").strip()
    scope = (skill.get("scope") or "project").strip()

    title = name.replace("-", " ").replace("_", " ").title()
    description = f"{trigger}. [GENERATED BY MOTIF — review and customize]"

    lines = [
        "---",
        f"name: {_slug(name)}",
        f"description: {description}",
        "---",
        "",
        f"# {title}",
        "",
        "## Trigger Phrases",
        "",
        trigger or "(No trigger specified)",
        "",
        "## Instructions",
        "",
    ]

    for i, step in enumerate(steps, 1):
        lines.append(f"{i}. {step}")
    if not steps:
        lines.append("1. (Add steps based on your workflow)")

    lines.extend([
        "",
        "## Key Constraints",
        "",
        f"- Confidence: {confidence}",
    ])
    if evidence:
        lines.append(f"- Evidence: {evidence[0]}")
    lines.append(f"- Scope: {scope}")

    return "\n".join(lines) + "\n"


def generate_all(
    analysis: dict,
    output_dir: Path,
    project_name: str = "this project",
) -> dict[str, str]:
    """Generate all files to the output directory.

    Creates CLAUDE.md and skills/{scope}/{skill-name}/SKILL.md for each skill.
    Scope (project/user) is encoded in the path for deploy_files routing.

    Args:
        analysis: Parsed analysis dict.
        output_dir: Directory to write generated files (staging).
        project_name: Project name for CLAUDE.md.

    Returns:
        Dict of {relative_path: content} for all generated files.
    """
    output_dir = Path(output_dir)
    generated: dict[str, str] = {}

    # CLAUDE.md
    claude_content = generate_claude_md(analysis, project_name)
    generated["CLAUDE.md"] = claude_content

    # Skills
    skills = analysis.get("skills") or []
    for skill in skills:
        scope = (skill.get("scope") or "project").strip()
        name = (skill.get("name") or "unnamed").strip()
        slug = _slug(name)
        rel_path = f"skills/{scope}/{slug}/SKILL.md"
        generated[rel_path] = generate_skill_file(skill)

    return generated


def preview_generation(analysis: dict) -> str:
    """Return a human-readable summary of what would be generated (for --dry-run).

    Args:
        analysis: Parsed analysis dict.

    Returns:
        Summary string with counts and file names.
    """
    lines = [
        "## Generation Preview",
        "",
        "**Would generate:**",
        "",
    ]

    # CLAUDE.md
    lines.append("- `CLAUDE.md`")

    # Skills
    skills = analysis.get("skills") or []
    if skills:
        lines.append(f"- `skills/` ({len(skills)} skills):")
        for s in skills:
            scope = s.get("scope", "project")
            name = s.get("name", "unnamed")
            slug = _slug(name)
            lines.append(f"  - `skills/{scope}/{slug}/SKILL.md` ({name})")
    else:
        lines.append("- No skills")

    # Counts
    rules = analysis.get("rules") or []
    included_rules = [r for r in rules if (r.get("confidence") or "low") in ("high", "medium")]
    lines.extend([
        "",
        "**Summary:**",
        f"- {len(included_rules)} rules (high/medium confidence)",
        f"- {len(skills)} skills",
        f"- {len(analysis.get('improvement_areas') or [])} improvement areas",
    ])

    return "\n".join(lines) + "\n"


def deploy_files(
    generated_files: dict[str, str],
    output_dir: Path,
    project_dir: Path | None = None,
) -> list[str]:
    """Copy generated files from staging to their deployment locations.

    Project-scoped items go to project_dir/.cursor/
    User-scoped items go to ~/.cursor/

    Reads file content from output_dir (staging). If a file is missing in
    staging, falls back to content from generated_files dict when available.

    Args:
        generated_files: Dict of {relative_path: content} from generate_all.
        output_dir: Staging directory where files were written.
        project_dir: Project root for project-scoped files. If None, use cwd.

    Returns:
        List of deployed absolute paths.
    """
    output_dir = Path(output_dir)
    project_dir = Path(project_dir) if project_dir else Path.cwd()
    user_cursor = Path.home() / ".cursor"
    deployed: list[str] = []

    for rel_path in generated_files:
        parts = rel_path.replace("\\", "/").split("/")
        src = output_dir / rel_path

        # Prefer reading from staging; fall back to dict content
        if src.exists():
            content = src.read_text(encoding="utf-8")
        else:
            content = generated_files.get(rel_path, "")

        if rel_path == "CLAUDE.md":
            dest = project_dir / "CLAUDE.md"
        elif len(parts) >= 3 and parts[0] == "skills":
            scope = parts[1]  # "project" or "user"
            skill_name = parts[2]
            if scope == "user":
                dest = user_cursor / "skills" / skill_name / "SKILL.md"
            else:
                dest = project_dir / ".cursor" / "skills" / skill_name / "SKILL.md"
        else:
            dest = output_dir / rel_path

        dest.parent.mkdir(parents=True, exist_ok=True)
        dest.write_text(content, encoding="utf-8")
        deployed.append(str(dest.resolve()))

    return deployed
